                    <article class="article quediferencasentregamos">
                        <div class="grid-100">
                            <h1 class="grid-70 push-15">
                                Versus a norma dos estudos de mercado
                            </h1>
                        </div>
                        <div class="grid-70 push-15">
                            <div class="grid-100">
                                <p class="grid-50">
                                    No lugar de dar protagonismo aos consumidores a Indiz dá enfoque à maneira como os espaços físicos convidam e geram certos comportamentos. Por exemplo para indicarmos a um cliente retalhista a melhor forma de organizar uma loja, tanto compreendemos essa tipologia de retalho como a interação dos clientes nos seus espaços.
                                </p>
                                <p class="grid-50">
                                    No lugar de entregar o tema de um estudo a uma única campanha e à sua receção por um grupo ou uma amostra de consumidores, a Indiz prefere analisar uma campanha articulada com a comunicação da marca, da sua categoria e de todo um ambiente comunicacional.
                                </p>
                            </div>
                            
                            <div class="grid-100">
                                <p class="grid-50">
                                    No lugar de compreender um expositor como uma peça isolada a Indiz analisa um expositor como um dos elementos de um sistema de marca e como parte integrante de uma configuração de loja. 
                                </p>
                                <p class="grid-50">
                                    Para entendermos um centro comercial analisamos semelhanças e diferenças com outras áreas comerciais e o modo como se refletem nos movimentos dos utilizadores.
                                </p>
                            </div>
                            
                            <div class="grid-100">
                                <p class="grid-50">
                                    Para compreendermos um segmento de clientes, analisamos as notícias que os constroem como geração, o modo como a publicidade de diferentes marcas os inclui, as celebridades que os agregam em seu redor e trechos do seu discurso nas redes sociais. 
                                </p>
                                <p class="grid-50">
                                    Para entregarmos as melhores ideias a um anunciante que queira transformar um dado valor no seu novo eixo comunicacional, compreendemos como esse valor é definido pela cultura, compreendendo media, analisando as redes sociais, descodificando publicidade e decifrando programas de televisão.
                                </p>
                            </div>

                            <h1 class="grid-100">
                                Ou seja, a Indiz não aborda temas complexos ao nível do indivíduo ou do detalhe, da parte.
                                A Indiz mapeia temas complexos observando todo o sistema, a cultura, através dos media,
                                do sistema comunicacional de uma marca ou de uma zona comercial.
                            </h1>
                        </div>
