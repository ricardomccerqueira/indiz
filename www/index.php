                    <div class="grid-100">
                       <h1 class="grid-70 push-15">
                            A Indiz inaugurou em Portugal, em 2003, um novo segmento dentro dos estudos de mercado qualitativos, aplicando técnicas de semiótica para compreender, com profundidade, campanhas de publicidade, marcas e notícias de imprensa ou televisão.
                        </h1>
                    </div>
                    <div class="grid-70 push-15">
                        <p class="grid-30">
                            Este estudo inverte o processo habitual em Marketing, utilizando como amostra as peças de comunicação, às quais coloca questões sobre a cultura dos compradores e consumidores. Esta abordagem debruça-se sobre a comunicação transmitida, analisa as mensagens para se poder compreender como convencem os destinatários.
                        </p>
                        <p class="grid-30 push-5">
                            O conhecimento técnico, a constante curiosidade e abertura de espírito, garantiu o desenvolvimento de novas metodologias e o alargamento da oferta, tendo como pressuposto a análise do contexto em redor dos consumidores: "contexto-cultura e contexto-espaço".
                        </p>
                        <p class="grid-30 push-10">
                            Nenhum outro estudo de mercado permite compreender e influenciar uma tal amplitude de campos de comunicação: publicidade, folhetos, embalagens, notícias, redes sociais, montras, configurações de lojas, modos de exposição, projetos de arquitetura, programas de televisão, patrocínios, eventos e até celebridades.
                        </p>
                    </div>
                    
